package imran.learnings.concurrency;

/**
 * Wrapper for long
 */
public class LongWrapper
{
    private long myLong;

    public LongWrapper()
    {

        this.myLong = myLong;
    }

    public long getValue()
    {
        return myLong;

    }

    public void incrementValue()
    {
        myLong++;

    }

}
